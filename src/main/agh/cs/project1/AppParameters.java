package agh.cs.project1;

public class AppParameters
{
    /**
     * The width of the map.
     */
    int width;

    /**
     * The height of the map.
     */
    int height;

    /**
     * How many animals in the center we start with.
     */
    int amountOfStartAnimals;

    /**
     * How much energy the origin animals start with.
     */
    int startEnergy;

    /**
     * How much energy an animal has to use to move.
     */
    int moveEnergy;

    /**
     * How much energy an animal regains upon eating a plant.
     */
    int plantEnergy;

    /**
     * The ratio between the size of the jungle and the size of the rest of the map.
     */
    float jungleRatio;
}
